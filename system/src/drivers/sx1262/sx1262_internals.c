uint8_t sx1262_is_busy(void) {
	uint8_t out = 0;

	if (LL_GPIO_IsInputPinSet(GPIOC, LL_GPIO_PIN_7) == SX1262_BUSY_ACTIVE)
	{
		out = 1;
	}

	if (spi_is_busy() == 1)
	{
		out = 1;
	}

	return out;
}

